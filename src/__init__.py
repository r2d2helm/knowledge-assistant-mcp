# Knowledge Assistant MCP Server
#
# Modular package structure:
# - config.py: Configuration constants and path functions
# - utils.py: Utility functions, regex patterns, and validation
# - cache.py: VaultCache class for in-memory caching
# - search.py: Search and query functions
# - writer.py: Note writing functions
# - graph.py: Knowledge graph functions
# - tools.py: MCP tool handlers and server instance
# - main.py: Entry point and server initialization
# - server.py: Backward-compatible module exposing all public APIs
